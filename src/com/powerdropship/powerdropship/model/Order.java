package com.powerdropship.powerdropship.model;

import javax.xml.bind.annotation.XmlTransient;
import java.util.ArrayList;

@XmlTransient
public class Order {

    private String orderId;
    private String firstName;
    private String lastName;
    private String addressLine1;
    private String addressLine2;
    private String street;
    private String city;
    private String state;
    private String postalCode;
    private String country;
    private String billingEmail;
    private String shippingMethod;

    public ArrayList<OrderedProduct> productsOfTheOrder = new  ArrayList<OrderedProduct>();

    public Order(String orderId){
        this.orderId = orderId;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAddressLine1() {
        return addressLine1;
    }

    public void setAddressLine1(String addressLine1) {
        this.addressLine1 = addressLine1;
    }

    public String getAddressLine2() {
        return addressLine2;
    }

    public void setAddressLine2(String addressLine2) {
        this.addressLine2 = addressLine2;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getBillingEmail() {
        return billingEmail;
    }

    public void setBillingEmail(String billingEmail) {
        this.billingEmail = billingEmail;
    }

    public ArrayList<OrderedProduct> getProductsOfTheOrder() {
        return productsOfTheOrder;
    }

    public void setProductsOfTheOrder(ArrayList<OrderedProduct> productsOfTheOrder) {
        this.productsOfTheOrder = productsOfTheOrder;
    }

    public String getShippingMethod() {
        return shippingMethod;
    }

    public void setShippingMethod(String shippingMethod) {
        this.shippingMethod = shippingMethod;
    }
}

